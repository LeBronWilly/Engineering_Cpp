// prog3_10, 印出布林值
#include <iostream>
#include <cstdlib>
using namespace std;
int main(void)
{
   bool status=false;   	// 宣告布林變數status, 設值為false 
   cout << "status=" << status << endl;   
   status=1;            	// 設定status的值為1 
   cout << "status=" << status << endl;
   
   system("pause");
   return 0;
}
